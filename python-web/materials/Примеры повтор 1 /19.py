#1. Создание словарей
#Словари можно создавать с использованием фигурных скобок {} или функции dict().

# Пустой словарь
empty_dict = {}

# Словарь с элементами
my_dict = {"name": "John", "age": 25, "city": "New York"}

# Использование функции dict()
another_dict = dict(name="Alice", age=30, city="Los Angeles")
print(another_dict)  # {'name': 'Alice', 'age': 30, 'city': 'Los Angeles'}